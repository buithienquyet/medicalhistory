
package com.eighteengroup.medicalhistory.models;

/**
 *
 * @author Hoa
 */
public class Doctor {
    private int doctorId;
    private String doctorName;
    private String doctorBirthday;
    private String doctorDepartment;
    private String doctorPosition;
    private String doctorCrestedDate;
    private String doctorUpdatedDate;

    public int getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(int doctorId) {
        this.doctorId = doctorId;
    }

    public String getDoctorName() {
        return doctorName;
    }

    public void setDoctorName(String doctorName) {
        this.doctorName = doctorName;
    }

    public String getDoctorBirthday() {
        return doctorBirthday;
    }

    public void setDoctorBirthday(String doctorBirthday) {
        this.doctorBirthday = doctorBirthday;
    }

    public String getDoctorDepartment() {
        return doctorDepartment;
    }

    public void setDoctorDepartment(String doctorDepartment) {
        this.doctorDepartment = doctorDepartment;
    }

    public String getDoctorPosition() {
        return doctorPosition;
    }

    public void setDoctorPosition(String doctorPosition) {
        this.doctorPosition = doctorPosition;
    }

    public String getDoctorCrestedDate() {
        return doctorCrestedDate;
    }

    public void setDoctorCrestedDate(String doctorCrestedDate) {
        this.doctorCrestedDate = doctorCrestedDate;
    }

    public String getDoctorUpdatedDate() {
        return doctorUpdatedDate;
    }

    public void setDoctorUpdatedDate(String doctorUpdatedDate) {
        this.doctorUpdatedDate = doctorUpdatedDate;
    }
    
}
